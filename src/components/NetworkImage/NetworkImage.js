import {useEffect, useRef, useState} from "react";
import './NetworkImage.scss'

const NetworkImage = ({activeNetwork, scrollToImage, tabContainer}) => {
    const [loading, setLoading] = useState(false)
    useEffect(() => {
        // scrollToImage()
        scrollToImage()
    }, [loading])

    const handleImageLoaded = () => {
        setLoading(true)
    }


    return (
        <div className={'result-container'}>
            <img alt={`image generated by ${activeNetwork.name}`} onLoad={handleImageLoaded}
                 src={activeNetwork.image}/>
        </div>
    )
}
export default NetworkImage